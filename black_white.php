                    <h1>The Black and White Cats</h1>
                    <p class="long-text">
                        The colour patterns on black and white cat breeds are actually known by different names, 
                        the most common pattern variations are as follows:
                    </p>
                    <ul class="long-text">
                        <li>Locket: mostly black with just a small bit of white, usually located on the stomach or neck.</li>
                        <li>Mitted: black all over with only white feet which look like mittens.</li>
                        <li>Tuxedo: black body with white feet, chest, stomach and nose which makes the cat appear like they're wearing a tuxedo.</li>
                        <li>Bicoloured: half black and half white colouring.</li>
                        <li>Magpie: white cats with random black spotting.</li>
                        <li>Harlequin: similar to the magpie with random black spotting, but with a black tail as well.</li>
                        <li>Cap and saddle: coloured head with a saddle patch on the cat's back, these felines might also have a black tail.</li>
                        <li>Van: Splashes of black colouring between the ears with a black tail.</li>
                    </ul>
                    <p class="long-text">
                        The British Shorthair is an incredibly popular cat in England and where they're most commonly 
                        blue, but the breed can produce black and white cats too! Originally bred to keep rodents at 
                        bay in homes and on farms, Shorthair breeds have a thick, dense coat that was designed to 
                        keep them warm when living outside. They have kind, easy going personalities and get along 
                        famously with dogs and even children, therefore they're best suited to family life where they 
                        can get plenty of attention.    
                    </p>
                    <p class="long-text">
                        Cymrics are often thought to be a long-haired version of the Manx cat and much like them, 
                        they can be completely tailless. If this black and white cat breed has no tail whatsoever, 
                        the cats are referred to as 'rumpies', whereas if they have just a stump of a tail they are 
                        called 'stumpies'. The lack of tail is the result of a genetic mutation, which just happened 
                        to become a staple of the breed because of the remote location of the Isle of Man where they 
                        were bred. These black and white cats make kind and loving housemates but are very people 
                        orientated, so they're best suited to individuals that don't spend too much time out of the 
                        home.
                    </p>
                    <p class="long-text">
                        The Munchkin is known as the Dachshund of the cat world because they share a similar genetic 
                        mutation with their short legs. Their coats can be just about any colour or pattern, so they 
                        can be black and white cats as well. Lovers of the breed know that they have very cheeky 
                        personalities and according to Catster, some refer to them as magpies as they're known to 
                        pinch their owner's possessions and store them away.   
                    </p>
                    <p class="long-text">
                        Thought of in their homeland of Japan as the original lucky cat, the Japanese Bobtail is 
                        believed to bring good luck to anyone who shares their home. Most commonly found in a mixture
                        of black, red and white (thought to be a lucky colouring), they can also be a black and 
                        white cat breed. With their irresistibly cute pom-pom like tail and long hind legs, the 
                        Japanese Bobtail has an unusual appearance that's been captured in Japanese art for at 
                        least 1000 years.   
                    </p>
                    <p class="long-text">
                        Perhaps the most royal of all cat breeds, the Persian comes in a variety of colours and can 
                        even be a black and white cat. First brought to Europe in 1626 and favoured by Queen Victoria 
                        in the 19th century.     
                    </p>

