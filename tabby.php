                        want a red cat that has tiger stripes or one with spots or patches.     
                    </p>
                    <p class="long-text">
                        Many people believe that red cats have warm personalities that match their coat color, thinking
                        they tend to be mellow, loving cats. However, cats are individuals and you may find yours to 
                        be a bossy alpha cat or one with a fiery, hot temper. Cat personalities are not as typically 
                        linked to breeds as are those of dogs. They haven't been bred for skills and temperaments as 
                        dogs have. But if you are looking for a specific personality, start with researching what is 
                        usual for different cat breeds.  
                    </p>
                    <p class="long-text">
                        The reason a cat is red lies in the genes and chromosomes the cat has inherited. The study of 
                        genetics is so complicated and detailed that it takes many years of concentrated study to come 
                        even close to mastering it. The red color gene O codes for the production of the red pigment 
                        phaeomelanin. It is dominant, so it masks all of the other colors (such as black and variants 
                        of black). 
                    </p>
                    <p class="long-text">
                        The primary red color gene is carried on the X chromosome, and it is therefore sex-linked. A 
                        male cat usually has only one X chromosome as it is paired with the Y chromosome and males are 
                        XY. A female cat has two X chromosomes and are XX.
                        The red gene may be the O variant, producing red pigment, or the o variant that does not. If a 
                        male inherits the O variant from his mother, he will be red. If he inherits the o, he instead 
                        can express the black pigment coded on other genes. He won't ever be both red and black (which 
                        would make him calico or tortoiseshell), except in unusual cases of inheriting multiple X 
                        chromosomes or merging with a twin before birth. 
                    </p>
                    <p class="long-text">
                        All red cats are tabby cats. The agouti gene determines whether a cat has a tabby pattern or not, 
                        but the non-agouti gene doesn't work when the red pigment is expressed. Although some red cats 
                        (usually dilutes) may appear to be a solid color, upon closer examination, you will see the pale 
                        stripes, whorls, or spots of the tabby. You will also see the distinctive tabby facial markings, 
                        including the magnificent "M" of tabby cats. 
