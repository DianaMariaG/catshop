                    <p class="long-text">
                        The Bengal could never be called delicate. They’re athletes: agile and graceful with a
                        strong, muscular body, as befits a cat who looks as if they belong in the jungle.
                        Despite their wild appearance, Bengal cats are actually quite affectionate with their
                        human families. That said, they also have high energy and a fun-loving, playful side. 
                        They want to stay active and need a home that can match their energy.
                    </p>
                    <p class="long-text">
                        With his distinctive spotted coat and large size, the Bengal looks like a wild cat on 
                        the prowl, but although one of his ancestors is the small, wild Asian leopard cat, he’s 
                        a domestic cat through and through.
                    </p>
                    <p class="long-text">
                        Bengals take their name from the Asian leopard cat’s scientific name, Felis bengalensis.
                        They were created through crosses between an Asian leopard cat — which in the 1950s and 
                        into the 1960s could be purchased at pet stores — and domestic shorthairs. Jean Mill, a 
                        breeder in California, was the first to make such a cross, but not because she wanted to 
                        create a new breed. She had acquired a leopard cat and allowed her to keep company with a 
                        black tom cat so she wouldn’t be lonely. To her surprise, since she hadn’t thought the two 
                        species would mate, kittens resulted, and Mill kept a spotted female. Breeding her back to 
                        her father produced a litter of spotted and solid kittens.
                    </p>
                    <p class="long-text">
                        At about the same time, Dr. Willard Centerwall was crossing Asian leopard cats with domestic 
                        cats at Loyola University. The leopard cats were resistant to the feline leukemia virus, so 
                        researchers were interested in finding out if the trait could be passed on to hybrid offspring.
                    </p>
                    <p class="long-text">
                        Various breeders became interested in developing the cats as a breed. Mill was one of them. 
                        Changes in her life had caused her to give up cat breeding, but she was ready to begin again. 
                        She had acquired some of Dr. Centerwall’s hybrids and sought out suitable males to breed to 
                        them. One was an orange domestic shorthair that she found in India, of all places, and the 
                        other was a brown spotted tabby acquired from a shelter. Bengals today are considered to be 
                        one and the same with domestic cats, and any Bengal purchased should be at least four 
                        generations removed from any ancestors with wild bloodlines.
                        </p>
                    <p class="long-text">
                        The Bengal is highly active and highly intelligent. This makes him fun to live with, but he 
                        can sometimes be challenging. On the whole, the Bengal is a confident, talkative, friendly 
                        cat who is always alert. Nothing escapes his notice. He likes to play games, including fetch, 
                        and he’s a whiz at learning tricks. His nimble paws are almost as good as hands, and it’s a 
                        good thing he doesn’t have opposable thumbs or he would probably rule the world. Bored bengal 
                        cats can also adopt some unconventional (and slightly destructive) habits, including: turning 
                        light switches on and off, fishing seals out of drains and excitedly plucking CDs from your 
                        DVD player.
